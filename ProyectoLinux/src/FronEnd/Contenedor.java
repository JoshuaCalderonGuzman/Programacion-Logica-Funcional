    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int selecionaLaFila = TblContenido.getSelectedRow(); 
          if (selecionaLaFila != -1) {
              int id = Integer.parseInt(TblContenido.getValueAt(selecionaLaFila, 0).toString());
               ((DefaultTableModel) TblContenido.getModel()).removeRow(selecionaLaFila);
               CModificar modificar = new CModificar();
               
               Modificar mod = null;
            try {
                mod = new Modificar( id);
            } catch (ParseException ex) {
                Logger.getLogger(Contenedor.class.getName()).log(Level.SEVERE, null, ex);
            }
                mod.setVisible(true);
                this.setVisible(false);
          }else {
            JOptionPane.showMessageDialog(null, "Por favor Seleccione una fila.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

