    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int selecionaLaFila = TblContenido.getSelectedRow(); 
          if (selecionaLaFila != -1) {
              int id = Integer.parseInt(TblContenido.getValueAt(selecionaLaFila, 0).toString());
               ((DefaultTableModel) TblContenido.getModel()).removeRow(selecionaLaFila);
               CContenedor contenedor = new CContenedor();
               contenedor.Eliminar(id);
          }else {
            JOptionPane.showMessageDialog(null, "Por favor Seleccione una fila.");
        }
         
    }//GEN-LAST:event_btnEliminarActionPerformed

